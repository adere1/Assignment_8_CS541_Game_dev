package com.example.myapplication;

class Person {
    String name;
    String age;
    int photoId;
    String status;
    String appdate;
    String followdate;

    Person(String name, String age, int photoId, String status, String appdate,String followdate) {
        this.name = name;
        this.age = age;
        this.photoId = photoId;
        this.status = status;
        this.appdate = appdate;
        this.followdate = followdate;
    }
}
